def acf_pacf_plot(data: pd.DataFrame, lags=60):
    """
    Run acf/pacf test and plot acf/pacf lags of given DataFrame
    Args: 
        data: pd.DataFrame;
        lags: int;
    Return: 
        dict: acf/pacf test for each column of data
        plot: figure
    """
    from statsmodels.graphics.tsaplots import plot_acf, plot_pacf
    from statsmodels.tsa.stattools import acf, pacf

    figure, ax = plt.subplots(len(data.columns), 2, figsize = (2*4, len(data.columns)*2))
    k = 0
    results = {}
    for col in data.columns:
        # i,_ = coordinates[k]
        acf_values, acf_conf_intervals = acf(data[col], 
                                             nlags=lags, 
                                             alpha=0.05, 
                                             fft=True)
        plot_acf(data[col].dropna().values, 
                 lags=lags, 
                 ax=ax[k][0], 
                 zero=False, 
                 title=f'{col} ACF', 
                 auto_ylims=True)

        pacf_values, pacf_conf_intervals = pacf(data[col].dropna().values, 
                                                nlags=lags, 
                                                alpha=0.05, 
                                                method='ywm')
        plot_pacf(data[col].dropna().values, 
                  lags=lags, 
                  ax=ax[k][1], 
                  zero=False, 
                  title=f'{col} PACF', 
                  auto_ylims=True)
        results[col] = {"acf":{
                                "Lag": range(lags+1),
                                "ACF": acf_values,
                                "Lower_CI": acf_conf_intervals[:, 0],
                                "Upper_CI": acf_conf_intervals[:, 1]
                                },
                        "pacf":{
                                "Lag": range(lags+1),
                                "PACF": pacf_values, 
                                "Lower_CI": pacf_conf_intervals[:, 0],
                                "Upper_CI": pacf_conf_intervals[:, 1]
                                }
                        }
        k += 1
    plt.tight_layout()
    plt.show() 
    return results, figure

