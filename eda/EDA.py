def plot_ts(data: pd.DataFrame):
    """
    plot time series with a proper grid layout
    the input dataframe should has more than 3 columns
    """
    n = len(data.columns)
    j, i = int(np.floor(np.sqrt(n))), int(n // np.floor(np.sqrt(n)) + 1)
    coordinates = [[k, l] for k in range(i) for l in range(j)]
    fig, ax = plt.subplots(i, j, figsize=(j*6, i*6))
    k = 0
    for col in data.columns:
        i, j = coordinates[k]
        ax[i][j].plot(data[col])
        ax[i][j].set_title(col)
        k += 1
    plt.show()
    return fig

class POLY():
    def __init__(self, btc_only=True):
        """
        Args:
            if btc_only, then btc relevant questions will be selected;
            otherwise, all crypto question will be selected
        """
        poly = load_polymarket_data()
        self.btc_only = btc_only
        self.MARKETS = poly['markets']
        self.TOKENS = poly['tokens']
        self.ODDS = poly['odds_history']
           
    def _separate_market_df(self):
        """
        separate poly dataframes for feature extraction
        """

        # markets - as volume info only available in markets df (trades are from 2025)
        if self.btc_only:
            btc_markets_df = self.MARKETS[self.MARKETS['question'].str.contains("Bitcoin|BTC|btc",
                                                                                 case=False,
                                                                                 na=False)].copy()
            logging.info("processing bitcoin only market data")
        else:
            btc_markets_df = self.MARKETS[self.MARKETS['question'].str.contains("Bitcoin|BTC|btc|ETH|Ethereum|Solana|Crypto|Coin|Blockchain",
                                                                                 case=False, 
                                                                                 na=False)]
            logging.info("processing all crypto market data")
        btc_markets_df['created_at'] = pd.to_datetime(btc_markets_df['created_at']).dt.normalize()

        # separate bidding direction - bid up, bid down, neutral
        btc_mkt_downs = btc_markets_df[btc_markets_df['question'].str.contains("less|lower|dip",
                                                                                case=False, 
                                                                                na=False)].copy()
        new_df = btc_markets_df[~btc_markets_df['market_id'].isin(btc_mkt_downs['market_id'])]
        btc_mkt_ups = new_df[new_df['question'].str.contains("greater|Up|above|hit|reach|break", 
                                                                             case=False, 
                                                                             na=False)].copy()

        btc_mkt_neutrals = new_df[~new_df['market_id'].isin(btc_mkt_ups['market_id'])].copy()

        # direction column
        btc_mkt_ups['direction'] = 1
        btc_mkt_downs['direction'] = -1
        btc_mkt_neutrals['direction'] = 0

        return btc_mkt_ups, btc_mkt_downs, btc_mkt_neutrals

    def _merge_token_odds(self, markets:pd.DataFrame):
        """
        add corresponding token and odds message to markets dataframe
        """
        # find all tokens of each market_id and choose the first token_id
        # for binary questions , the first token_id means prob of 'Yes'
        tokens = self.TOKENS[
                        self.TOKENS['market_id'].isin(markets['market_id'])
                        ].drop_duplicates(
                            subset=['market_id'], 
                            keep='first')
        # markets['token_id'] = markets['market_id'].apply(lambda x : tokens[tokens['market_id']==x].iloc[0]['token_id']) -> slower than merge
        res = pd.merge(left=markets, 
                       right=tokens, 
                       how='left', 
                       left_on=['market_id'], 
                       right_on=['market_id'])

        # merge odds price (a probability) on same market_id and token_id
        # same combination of ['market_id', 'token_id'] may has multiple price(prob), we choose the mean of them
        ## since (1) volume info missed in TOKENS and ODDS dfs. (2) there is no reason for arbitrary selection.
        odds = self.ODDS[
                        self.ODDS['market_id'].isin(markets['market_id'])][
                            ['market_id', 'token_id', 'price']
                                ].drop_duplicates(
                                    subset=['market_id'], 
                                    keep='first')
        res = pd.merge(left=res, 
                       right=odds, 
                       how='left', 
                       left_on=['market_id', 'token_id'], 
                       right_on=['market_id', 'token_id'])
        res.fillna({'price':0.5}, inplace=True)
        res = res.groupby('token_id').agg(
                            market_id=('market_id','min'),
                            question=('question','min'),
                            created_at=('created_at','min'),
                            end_date=('end_date','min'),
                            volume=('volume', 'mean'),
                            direction=('direction', 'min'),
                            price=('price','mean')
                        ).reset_index()

        return res
    
    def _prepare_dfs(self):
        ups, downs, neutrals = self._separate_market_df()
        ups = self._merge_token_odds(ups)
        downs = self._merge_token_odds(downs)
        neutrals = self._merge_token_odds(neutrals)
        res = pd.concat([ups, downs, neutrals]).sort_values('created_at')
        res['weighted_volume'] = res['volume'] * res['direction'] * res['price'] * 2

        return res
    
    def prepare_features(self, use_benchmark=False):
        """
        prepare polymarket features based on markets counts and volumes
        if use_benchmark, use Trilemma's template and original volumes;
        otherwise use Trilemma's method with weighted volumes
        """
        df = self._prepare_dfs()
        if use_benchmark:
            logging.info("preparing polymarket features on Trilemma's method and original volume")
        else:
            df.drop('volume', axis=1, inplace=True)
            df.rename(columns={'weighted_volume': 'volume'}, inplace=True)
            logging.info("preparing polymarket faeatures on Trilemma's method and weighted volume")

        daily_stats = df.groupby('created_at').agg(
            daily_market_count=('market_id', 'count'),
            daily_volume=('volume', 'sum')
        ).reset_index()
        daily_stats['created_at'] = pd.to_datetime(daily_stats['created_at']).dt.normalize()
        daily_stats = daily_stats.set_index('created_at').sort_index()
        daily_stats["market_count_pct"] = (
            daily_stats["daily_market_count"]
            .rolling(30, min_periods=1)
            .apply(lambda x: (x.iloc[-1] > x[:-1]).sum() / max(len(x) - 1, 1) if len(x) > 1 else 0.5)
        )
        daily_stats["volume_pct"] = (
            daily_stats["daily_volume"]
            .rolling(30, min_periods=1)
            .apply(lambda x: (x.iloc[-1] > x[:-1]).sum() / max(len(x) - 1, 1) if len(x) > 1 else 0.5)
        )

        # match btc price data
        logging.info("matching bitcoin price data from coinmetrics dataset")
        btc = load_data()
        btc = btc.loc[daily_stats.index.min():]
        daily_stats = pd.merge(left=btc[['PriceUSD_coinmetrics', 'CapMVRVCur']], 
                               right=daily_stats, 
                               how='left',
                               left_index=True,
                               right_index=True).ffill() # forward fill for discontinuity of market data
        daily_stats['price_pct'] = (
            daily_stats['PriceUSD_coinmetrics']
            .rolling(30, min_periods=1)
            .apply(lambda x: (x.iloc[-1] > x[:-1]).sum() / max(len(x) - 1, 1) if len(x) > 1 else 0.5)
        )
        daily_stats['MVRV_pct'] = (
            daily_stats['CapMVRVCur']
            .rolling(30, min_periods=1)
            .apply(lambda x : (x[-1] > x[:-1]).sum() / max(len(x) - 1, 1) if len(x) > 1 else 0.5)
        )
        return daily_stats
    

