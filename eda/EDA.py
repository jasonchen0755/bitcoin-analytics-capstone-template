def differatiate_data(data: pd.DataFrame, period: int) -> pd.DataFrame:
    """
    return a de-mean-ed percentage change df of input df
    Args:    
        data: a pandas dataframe
        period: period interval, int
    return: pd.DataFrame
        percentage change of dataframe on assigned interval
        drop na, and substract mean
    """
    res = data.pct_change(periods=period, fill_method=None).dropna()
    res = res - res.mean()
    return res

def AD_Fuller_Test(data: pd.DataFrame) -> Dict:
    """
    Run Augment Dickey-Fuller test for each column of input df
    """
    from statsmodels.tsa.stattools import adfuller

    result = {}
    for col in data.columns:
        res = adfuller(data[col].dropna().values)
        result[col] = {"AD_Fuller_Statistics": res[0], "pvalue": res[1]}
    result["critic value"] = {k: np.round(v,3) for (k,v) in res[4].items()}
    return result

def vec_ar_test(data: pd.DataFrame, y: str, alpha=0.05):
    """
    Run vector auto-regressive test after stationary check and optimal lags finding
    Args:
        data: datasets;
        y: target variable;
        alpha: significance level for AD_Fuller test
    Return:
        VAR model fitting result
    """
    from statsmodels.tsa.api import VAR

    # check stationary
    adf = AD_Fuller_Test(data)
    for col in data.columns:
        if adf[col]['pvalue'] >  alpha:
            print(f"feature {col} does not pass AD_Fuller test at significance level {alpha}")
            data.drop(col, axis=1, inplace=True)
    
    # find optimal bic lags
    model = VAR(data.dropna())
    finding_lags = model.select_order(maxlags=30)
    optimal_lags = finding_lags.bic
    print(f"The optimal BIC lags number is {optimal_lags}")
    results = model.fit(maxlags=optimal_lags)

    return results

def granger_test(data: pd.DataFrame, y: str, lags: int) -> Dict:
    """
    return Granger Causality Test result of features on y;
    both features and y should be stationary;
    Args:
        data: all data include target y
        y: target column name in data
        lags: maximum lags to run test
    return:
        test results
    """
    from statsmodels.tsa.stattools import grangercausalitytests

    feature_names = data.columns
    results = {}
    for feat in feature_names:
        if feat != y:
            print(f"=====================================================================\nTesting Granger Causality from {feat} to {y}")
            gc = grangercausalitytests(data[[feat, y]], maxlag=lags)
            results[feat] = gc
    return results

def johansen_coint_test(data:pd.DataFrame, det_order=0, k_ar_diff=1):
    """
    Run Johansen cointegration test
    Args:
        data: should contain no more than 12 variables
    Return:
        coint_rank: int
        result: Johansen cointegration test result
    """
    from statsmodels.tsa.vector_ar.vecm import coint_johansen

    result = coint_johansen(data, det_order=det_order, k_ar_diff=k_ar_diff)

    # for i in range(len(data.columns)):
    #     print(f"Rank {i}: trace={result.lr1[i]:.3f}, critical={result.cvt[i, 1]:.3f}")
    
    # for i in range(len(data.columns)):
    #     print(f"Rank {i}: max eig={result.lr2[i]:.3f}, critical={result.cvm[i, 1]:.3f}")
    
    coint_rank = "Not defined"
    for r in range(len(data.columns)):
        if result.lr1[r] < result.cvt[r, 1]:  
            coint_rank = r
            break
            
    return coint_rank, result

def vecm(data:pd.DataFrame, r:int):
    """
    Run VECM model fitting
    Arges:
        data: contains no more than 12 variables; should be same data for coint_test;
        r: cointegration rank obtained from coint_test
    return:
        fitted model
    """
    from statsmodels.tsa.vector_ar.vecm import VECM, select_order
    
    lag_selection = select_order(data, maxlags=30, deterministic='co')
    optimal_lags = lag_selection.bic
    print(f"Optimal lag length for VAR: {optimal_lags}")
    print(f"VECM will use {optimal_lags - 1} lags of differences")

    vecm_model = VECM(
        endog=data,
        k_ar_diff=optimal_lags - 1,  
        coint_rank=r,                
        deterministic='co'           
    )
    
    return vecm_model.fit()

